const DEFAULT_SLOTS = 5;

function getSlotCount(committeeId) {
    const links = COMMITTEE_PROJECT_LINKS[committeeId];
    return links ? Object.keys(links).length : DEFAULT_SLOTS;
}

export function getLinksForCommittee(committeeId) {
    return COMMITTEE_PROJECT_LINKS[committeeId] || {};
}

export function assignProjectNumbers(topics = [], committeeId) {
    const maxSlots = getSlotCount(committeeId);
    const used = new Set();
    const enriched = topics.map((t) => {
        const slot = Number(t.projectNumber || t.projectSlot || t.slot);
        return slot ? { ...t, projectNumber: slot } : { ...t };
    });

    // Mark used slots from existing data
    enriched.forEach((t) => {
        if (t.projectNumber && t.projectNumber >= 1 && t.projectNumber <= maxSlots) {
            used.add(t.projectNumber);
        }
    });

    // Assign the first free slot to topics without one
    let next = 1;
    return enriched.map((t) => {
        if (!t.projectNumber || t.projectNumber < 1) {
            while (used.has(next) && next <= maxSlots) next++;
            t.projectNumber = next <= maxSlots ? next : next;
            used.add(t.projectNumber);
            next++;
        }
        return t;
    });
}

export function getNextProjectSlot(topics = [], committeeId) {
    const maxSlots = getSlotCount(committeeId);
    const used = new Set();
    topics.forEach((t) => {
        const slot = Number(t.projectNumber);
        if (slot) used.add(slot);
    });
    for (let i = 1; i <= maxSlots; i++) {
        if (!used.has(i)) return i;
    }
    return used.size + 1;
}

export function getDocLinksForTopic(committeeId, topic) {
    if (!topic) return {};
    const linksByCommittee = getLinksForCommittee(committeeId);
    const slot = Number(topic.projectNumber || topic.projectSlot || topic.slot);
    return linksByCommittee[slot] || {};
}

export function getDocUrl(committeeId, topic, docType) {
    const links = getDocLinksForTopic(committeeId, topic);
    if (!links) return '';
    if (docType === 'folder') return links.folder || '';
    if (docType === 'word' || docType === 'doc') return links.doc || '';
    if (docType === 'ppt') return links.ppt || '';
    return '';
}
